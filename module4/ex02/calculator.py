#!/usr/bin/env python3

first = int(input("Give me the first number: "))
sec = int(input("Give me the second number: "))

print(f"Thank you!")

print(f"{first} + {sec} = {first + sec}")
print(f"{first} - {sec} = {first - sec}")
print(f"{first} / {sec} = {first // sec}")
print(f"{first} * {sec} = {first * sec}")
